package unit2;

// 给出汉字 “你” “我” “他” 在 Unicode 表中的位置
public class solution2_1 {
    public static void main(String[] args) {
        char you, me, he;
        you = '你';
        me = '我';
        he = '他';
        System.out.println(you + "的位置:" + ((int)you));
        System.out.println(me + "的位置:" + ((int)me));
        System.out.println(he + "的位置:" + ((int)he));
    }
}
